document.addEventListener('DOMContentLoaded', function () {
	let clipboard = new ClipboardJS('.copy-button');
	

	clipboard.on('success', function (e) {
		e.trigger.innerHTML = '<i class="fas fa-check"></i>';
		setTimeout(function () {
			e.trigger.innerHTML = '<i class="fas fa-copy"></i>';
		}, 2000);
	});



	clipboard.on('error', function (e) {
		console.error('Copy failed', e);
	});

	const selectContents = document.querySelectorAll('.select-content');
	const contentD = document.getElementById('content');
	const spinner = document.getElementById('spinner');
	spinner.classList.add("d-none")

	const urlParams = new URLSearchParams(window.location.search);
	const contentUrl = urlParams.get('content');

	const sidebarContent = document.getElementById("sidebarContent");
	const openSideBar = document.getElementById("openSideBarContent");
	const closeSideBar = document.getElementById("closeSideBarContent");

	async function selectContent(event) {
		spinner.attributes.display = "block"
		selectContents.forEach(tab => tab.classList.remove('select-active'));

		event.currentTarget.classList.add('select-active');
		spinner.classList.add("d-block")

		const content = event.currentTarget.getAttribute('data-content');
		let contentPath = content.replace("_", "/")
		const data = await getContent("0.6.x", contentPath)

		spinner.classList.remove("d-block")
		spinner.classList.add("d-none")

		if (data) {
			contentD.innerHTML = data;
		} else {
			contentD.innerHTML = "<h5>Data Null</h5>"
		}

		contentD.scrollTo({
			top: 0,
			behavior: 'smooth'
		});

		updateURL(content)
		if (window.innerWidth <= 760) {
			closeSideBar.style.display = "none";
			sidebarContent.style.display = "none";
			openSideBar.style.display = "block";
		}
	}

	selectContents.forEach(async (sc) => {
		sc.addEventListener('click', selectContent);
		const dataContent = sc.getAttribute('data-content');
		
		if (dataContent === contentUrl) {
			sc.click()
		}
	});

	if (!contentUrl) {
		selectContents[0].click()
	}

	openSideBar.onclick = function (event) {
		event.currentTarget.style.display = "none";
		closeSideBar.style.display = "block";
		sidebarContent.style.display = "block";
	};

	closeSideBar.onclick = function (event) {
		event.currentTarget.style.display = "none";
		sidebarContent.style.display = "none";
		openSideBar.style.display = "block";
	};

	document.getElementById("search").addEventListener('input', (event) => {
		const searchValue = event.target.value.toLowerCase();
		const elementSideContent = document.getElementById("sidebarContentElement");

		const items = elementSideContent.querySelectorAll(".select-content");

		items.forEach(item => {
			const textContent = item.textContent.toLowerCase();
			const parent = item.closest("li");

			if (textContent.includes(searchValue)) {
				parent.style.display = "";
			} else {
				parent.style.display = "none";
			}
		});
	});

	document.getElementById("clearSearch").addEventListener('click', () => {
		const searchInput = document.getElementById("searchInput");
		const elementSideContent = document.getElementById("sidebarContentElement");

		searchInput.value = ""; 

		const items = elementSideContent.querySelectorAll(".select-content");
		items.forEach(item => {
			const parent = item.closest("li");
			parent.style.display = "";
		});
	});


		const htmlSecurity = `<form id="quickForm" method="POST">
	<%- $_csrf.input(csrfToken) %>
	<input type="text" name="username">
	<button type="submit">Save</button>
</form>
`
		
		const htmlCSRF = `<form action="https://your-application.com/user/email" method="POST">
    <input type="email" value="malicious-email@example.com">
</form>

<script>
    document.forms[0].submit();
</script>`
		const htmlPreventCSRF = `<form method="POST" action="/profile">
	<%- $_csrf.input(csrfToken) %>

    <!-- Equivalent to... -->
    <input type="hidden" name="_csrf" value="<%= csrfToken %>">
</form>`

		const htmlEmail = `<!-- @isNotifications/email/demoEmail.ejs -->
<!DOCTYPE html>
<html lang="en">

<head>
	<meta charset="UTF-8">
	<meta http-equiv="X-UA-Compatible" content="IE=edge">
	<meta name="viewport" content="width=device-width, initial-scale=1.0">
	<title>Demo</title>
</head>

<body>
	<div style="font-family: Arial, sans-serif; max-width: 600px; margin: 0 auto;">
		<h2 style="color: black;">Welcome</h2>
		<p>Template is generated by Bamimi</p>
	</div>
</body>

</html>
`
		
		
		setTimeout(() => {
			document.getElementById('html-code').textContent = htmlSecurity
		}, 1500)

		setTimeout(() => {
			document.getElementById('html-code-csrf').textContent = htmlCSRF
			document.getElementById('html-code-prevent-csrf').textContent = htmlPreventCSRF
		}, 1500)
	
		setTimeout(() => {
			document.getElementById('html-mail').textContent = htmlEmail
		}, 1500)

})
